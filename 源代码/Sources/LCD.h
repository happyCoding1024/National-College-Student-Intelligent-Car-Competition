/*--------------------------------------------
LCD_write_byte: 使用SPI接口写数据到LCD
输入参数：dt：写入的数据；
command ：写数据/命令选择；
编写日期：20080918 
----------------------------------------------*/
void LCD_write_byte(unsigned char dt, unsigned char command)
{
	unsigned char i; 
	PORTA_PA0=0;	 //sce
	if(command==0)
	PORTA_PA2=0;//dc
	else 
		PORTA_PA2=1;//dc	
	for(i=0;i<8;i++)
	{ 
		if(dt&0x80)
			PORTA_PA3=1;//sdin
		else
			PORTA_PA3=0; //sdin
		dt=dt<<1;		
		PORTA_PA4=0; //sclk 		
		PORTA_PA4=1; //sclk
	}	
//	PORTB_PB5=1;//dc	
	PORTA_PA1=1;//sce	
//	PORTB_PB4=1;//sdin
}
